# Operadores Aritméticos

a = 10
b = 3

print('***OPERADORES ARITMÉTICOS***')
# Suma +
suma = a + b
print(f'Suma: {suma}')

# Resta -
resta = a- b
print(f'Resta: {resta}')

# Multiplicación *
multi = a * b
print(f'Multiplicación: {multi}')

# División /, retorna tipo flotante
div = a / b
print(f'División Flotante con 2 decimales: {div:.5f}')

# División Entera //
div_entera = a // b
print(f'División entera: {div_entera}')

# Residuo o Módulo de división %
modulo = a%b
print(f'Módulo: {modulo}')

# Exponente (potenciación)
potencia = a ** b
print(f'Exponenciación: {potencia}')