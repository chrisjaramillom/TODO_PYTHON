# OPERADORES DE COMPARACIÓN

print('***OPERADORES DE COMPARACIÓN***')

a, b, c = 7, 5, 7
print(f'Valores Iniciales a: {a}, b: {b}, c: {c}')

# Operador de Igualdad ==
resultado = a == b
resultado2 = a == c
print(f'Resultado de a==b es: {resultado} y a==c es: {resultado2}')

# Operador de Diferente !=
resultado = a != b
resultado2 = a != c
print(f'Resultado de a!=b es: {resultado} y a!=c es: {resultado2}')

# Operador de Mayor que >
resultado = a > b
resultado2 = a > c
print(f'Resultado de a>b es: {resultado} y a>c es: {resultado2}')

# Operador de Mayor o igual que >=
resultado = a >= b
resultado2 = a >= c
print(f'Resultado de a>=b es: {resultado} y a>=c es: {resultado2}')

# Operador de Menor que <
resultado = a < b
resultado2 = a < c
print(f'Resultado de a<>=>b es: {resultado} y a<c es: {resultado2}')

# Operador de Mayor o igual que >=
resultado = a <= b
resultado2 = a <= c
print(f'Resultado de a<=b es: {resultado} y a<=c es: {resultado2}')