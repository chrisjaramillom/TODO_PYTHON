# Declarar la variable
# Agregar el ciclo for
# Imprimir la cantidad de vocales encontradas en la cadena

cadena = 'Hola Mundo'
cont_vocales = 0
for vocales in cadena:
    if vocales == 'a' or vocales == 'e' or vocales == 'i' or vocales == 'o' or vocales == 'u':
        cont_vocales += 1
print(cont_vocales)

